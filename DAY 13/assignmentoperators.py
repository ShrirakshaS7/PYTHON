# Assignment Operators

# +=(add and assign)
a=6
a+=2
print(a)

# without +=

a=6
a=a+2
print(a)

# -=(substract and assign)

a=6
a-=2
print(a)

# *=(multiply and assign)

a=9
a*=2
print(a)

# /=(divide and assign)

a=48
a/=3
print(a)

# //=(divide and assign)

a=48
a//=3
print(a)

# %=(modules and assign)

a=6
a%=2
print(a)

a=7
a%=2
print(a)

# **=(power and assign)

a=6   
a**=2
print(a)